    private void jButtonsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsimpanActionPerformed
        // TODO add your handling code here:
        try {
          String nama =jTextFieldnama.getText();
            String jenis =jTextFieldjenis.getText();
            int jumlah =Integer.parseInt(jTextFieldjumlah.getText());
            mp.setNamabarang(nama);
            mp.setJenisbarang(jenis);
            mp.setJumlahbarang(jumlah);
            cp.simpandata(mp);
            datatable();
            bersih();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"harap masukan data" );
        }
    }//GEN-LAST:event_jButtonsimpanActionPerformed

