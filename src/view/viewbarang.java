    private void datatable() {
        koneksi.koneksidatabase();

        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("id");
        tabel.addColumn("nama");
        tabel.addColumn("jenis");
        tabel.addColumn("jumlah");
        jTable3.setModel(tabel);
        try {
            String data = "select *from tbl_barang";
            ResultSet res = koneksi.state.executeQuery(data);
            while (res.next()) {
                tabel.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nama"),
                    res.getString("jenis"),
                    res.getString("jumlah")

                });
            }
        } catch (Exception e) {
        }

    }

